package javaClass5.loopStatement;

public class WhileLoopTest {
    public static void main(String[] args) {
        // while loop
        // increment
        int i=0;   // initial value;
        while (i<6){   // condition
            System.out.println(i);
            i++;  // increment
        }
        System.out.println("+++++++++++++++++++++++++++++++++++++");
        // while loop
        // increment
        int j=10;   // initial value;
        while (j>5){   // condition
            System.out.println(j);
            j--;  // Decrement
        }
        System.out.println("+++++++++++++++++++++++++++++++++++++");

    }
}
