package javaClass5.loopStatement;

public class DoWhileLoopTest {
    public static void main(String[] args) {

        // Do While Loop
        // Decrement
        int k =10;  // value
        do {  // print 1st
            System.out.println(k);
            --k; // increment
        }while (k>=6);  // condition


        System.out.println("+++++++++++++++++++++++++++++++++++++");

        // Do While Loop
        // Increment
        int m =10;  // value
        do {  // print 1st
            System.out.println(m);
            m++; // increment
        }while (m<=20);  // condition

        System.out.println("+++++++++++++++++++++++++++++++++++++");

    }
}
