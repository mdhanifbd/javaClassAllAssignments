package javaClass2;

public class RelationalOperatorTest {
    public static void main(String[] args) {
        int num1 = 33 , num2 = 66;

        System.out.println(num1 != num2); // 33 is not equal 66 so it is true.
        System.out.println(num1 == num2); // 33 is equal 66 so it is false.
        System.out.println(num1 > num2);  // 33 is grater than 66 so it is false;
        System.out.println(num1 < num2);  // 33 is smaller than 66 so it is true;
        System.out.println(num1 >= num2); // 33 is grater or equal to 66 so it is false;
        System.out.println(num1 <= num2); // 33 is smaller or equal to 66 so it is true.


    }







}
