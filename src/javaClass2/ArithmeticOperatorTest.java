package javaClass2;

public class ArithmeticOperatorTest {



    public static void main(String[] args) {

        int num1 = 22, num2 = 33, num3 =44;

        num3 = num1 - num2;  // 22-33 = -11;
        System.out.println(num3); // it will print -11 and num3 current value is -11.

        num3 = num2 + num1;  // 33+22 = -55;
        System.out.println(num3); // it will print 55 and num3 current value is 55.


        num3 = num1 * num2;  // 22*33 = 726;
        System.out.println(num3); // it will print -11 and num3 current value is 726.

        num3 = num1 / num2;  // 22/33 = 0;
        System.out.println(num3); // it will print -11 and num3 current value is 0.

        num3 = num1 % num2;  // 22%33 = 22;
        System.out.println(num3); // it will print -11 and num3 current value is 22.



    }



}
